#include "date.h"
#include <time.h>

//#####################################################################################################//
// long CDate::getDateJJJJMMDD()
//#####################################################################################################//
long CDate::getDateJJJJMMDD()
{
    tm     * t;        
	time_t   tt;

	tt = time(0L);   
	t = localtime(&tt); /* Ortszeit */

	return (    (long)(t->tm_year + 1900) * 10000L +
		        (long)(t->tm_mon  +    1) *   100L +
			    (long)(t->tm_mday       )            );
}
